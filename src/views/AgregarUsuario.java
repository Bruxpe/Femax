    private void tblUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuarioMouseClicked
        // TODO add your handling code here:
        int row = tblUsuario.getSelectedRow();
        if(row >= 0){
            btnUpdate.setEnabled(true);
            btnSave.setEnabled(false);
            String id = tblUsuario.getModel().getValueAt(row, 0).toString();
            idSelected = id;
            UsuarioRepositorio usuarioRepo = new UsuarioRepositorio();
            Usuario v = usuarioRepo.Get(Integer.parseInt(id));            
            inputUsuario.setText(v.usuario);
            inputContrasenia.setText(v.contrasenia);
            inputNombres.setText(v.nombres);
            inputApellidos.setText(v.apellidos  );
            inputTelefono.setText(v.telefono);
            inputCorreo.setText(v.correo);
        }
    }//GEN-LAST:event_tblUsuarioMouseClicked

