    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        ConexionBD con = new ConexionBD();
        String usuario = inputUsuario.getText();
        String contrasenia = inputContrasenia.getText();
        String nombres = inputNombres.getText();
        String apellidos = inputApellidos.getText();
        String telefono = inputTelefono.getText();
        String correo = inputCorreo.getText();
        String rol = inputRol.getSelectedItem().toString();
        
        inputUsuario.setBackground(Color.WHITE);
        inputContrasenia.setBackground(Color.WHITE);
        inputNombres.setBackground(Color.WHITE);
        inputApellidos.setBackground(Color.WHITE);
        inputTelefono.setBackground(Color.WHITE);
        inputCorreo.setBackground(Color.WHITE);
        
        if (usuario.isEmpty() || contrasenia.isEmpty() || apellidos.isEmpty() || telefono.isEmpty() || correo.isEmpty()){
            
            if(usuario.isEmpty()){
                inputUsuario.setBackground(new Color(255,153,153));
            }
            if(contrasenia.isEmpty()){
                inputContrasenia.setBackground(new Color(255,153,153));
            }
            if(nombres.isEmpty()){
                inputNombres.setBackground(new Color(255,153,153));
            }
            if(apellidos.isEmpty()){
                inputApellidos.setBackground(new Color(255,153,153));
            }
            if(telefono.isEmpty()){
                inputTelefono.setBackground(new Color(255,153,153));
            }
            if(correo.isEmpty()){
                inputCorreo.setBackground(new Color(255,153,153));
            }
        }else{
            
            UsuarioRepositorio usuarioRepo = new UsuarioRepositorio();

            Usuario usuarioToSave = new Usuario();
            usuarioToSave.usuario = usuario;
            usuarioToSave.contrasenia = contrasenia;
            usuarioToSave.nombres = nombres;
            usuarioToSave.apellidos = apellidos;
            usuarioToSave.telefono = telefono;
            usuarioToSave.correo = correo;
            usuarioToSave.rol = rol;
            

            try {
                usuarioRepo.crearUsuario(usuarioToSave);
                JOptionPane.showMessageDialog(null, "Se guardo exitosamente");
                mostrarUsuarios();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "No se pudo guardar, intentelo de nuevo");
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblUsuario.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(null, "Debe seleccionar un elemento de la tabla para eliminar");
        }
        else{
            try {
                UsuarioRepositorio usuarioRepo = new UsuarioRepositorio();
                boolean delete = usuarioRepo.eliminarUsuario(Integer.parseInt(idSelected));
                if(delete){
                    inputUsuario.setText("");;
                    inputContrasenia.setText("");;
                    inputNombres.setText("");;
                    inputApellidos.setText("");;
                    inputTelefono.setText("");;
                    inputCorreo.setText("");;
                    idSelected = null;
                    btnUpdate.setEnabled(false);
                    btnSave.setEnabled(true);
                    JOptionPane.showMessageDialog(null, "Se elimino exitosamente");
                    mostrarUsuarios();
                }
                else{
                    JOptionPane.showMessageDialog(null, "No se pudo eliminar, intentelo de nuevo");
                }
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "No se pudo eliminar, intentelo de nuevo");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

