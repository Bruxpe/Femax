    public void mostrarUsuarios(){
            
        try {
            UsuarioRepositorio usuarioRepo = new UsuarioRepositorio();
            List<Usuario> usuario = usuarioRepo.List();
            DefaultTableModel DFT = (DefaultTableModel) tblUsuario.getModel();
            DFT.setRowCount(0);
                for(Usuario u:usuario){
                    Vector v = new Vector();
                    for (int ii = 1; ii <= usuario.size(); ii++) {
                        v.add(u.id_usuario);
                        v.add(u.usuario);
                        v.add(u.nombres);
                        v.add(u.apellidos);
                        v.add(u.telefono);
                        v.add(u.correo);
                        v.add(u.rol);
                    }
                DFT.addRow(v);
                }
            } catch (Exception e) {
                System.out.println(e);
        }
    }

