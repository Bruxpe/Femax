    public void mostrarTipoAlquiler(){
            
        try {
            TipoAlquilerRepositorio tipoAlquilerRepo = new TipoAlquilerRepositorio();
            List<TipoAlquiler> alquiler = tipoAlquilerRepo.List();
            DefaultTableModel DFT = (DefaultTableModel) tblTipoAlquiler.getModel();
            DFT.setRowCount(0);
                for(TipoAlquiler tipoalq:alquiler){
                    Vector v = new Vector();
                    for (int ii = 1; ii <= alquiler.size(); ii++) {
                        v.add(tipoalq.id_tipo_alquiler);
                        v.add(tipoalq.nombre);                    
                    }
                DFT.addRow(v);
                }
            } catch (Exception e) {
                System.out.println(e);
        }
    }

