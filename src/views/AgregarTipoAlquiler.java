    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        ConexionBD con = new ConexionBD();
        String nombre = inputNombre.getText();

        if (nombre.isEmpty()){
            inputNombre.setBackground(new Color(255,153,153));
        } else {

            TipoAlquilerRepositorio tipoAlquiler = new TipoAlquilerRepositorio();

            TipoAlquiler tipoAlquilerToSave = new TipoAlquiler();
            tipoAlquilerToSave.nombre = nombre;

            try {
                tipoAlquiler.crearTipoAlquiler(tipoAlquilerToSave);
                JOptionPane.showMessageDialog(null, "Se guardo exitosamente");
                mostrarTipoAlquiler();
            } catch (SQLException ex){
                JOptionPane.showMessageDialog(null, "No se pudo guardar, intentelo de nuevo");
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblTipoAlquiler.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(null, "Debe seleccionar un elemento de la tabla para eliminar");
        }
        else{
            try {
                TipoAlquilerRepositorio tipoAlquilerRepo = new TipoAlquilerRepositorio();
                boolean delete = tipoAlquilerRepo.eliminarTipoAlquiler(Integer.parseInt(idSelected));
                if(delete){
                    inputNombre.setText("");;
                    idSelected = null;
                    btnUpdate.setEnabled(false);
                    btnSave.setEnabled(true);
                    JOptionPane.showMessageDialog(null, "Se elimino exitosamente");
                    mostrarTipoAlquiler();
                }
                else{
                    JOptionPane.showMessageDialog(null, "No se pudo eliminar, intentelo de nuevo");
                }
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "No se pudo eliminar, intentelo de nuevo");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tblTipoAlquilerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTipoAlquilerMouseClicked
        // TODO add your handling code here:
        int row = tblTipoAlquiler.getSelectedRow();
        if(row >= 0){
            btnUpdate.setEnabled(true);
            btnSave.setEnabled(false);
            String id = tblTipoAlquiler.getModel().getValueAt(row, 0).toString();
            idSelected = id;
            TipoAlquilerRepositorio tipoAlquilerRepo = new TipoAlquilerRepositorio();
            TipoAlquiler v = tipoAlquilerRepo.Get(Integer.parseInt(id));            
            inputNombre.setText(v.nombre);
        }
    }//GEN-LAST:event_tblTipoAlquilerMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String nombre = inputNombre.getText();
        
        inputNombre.setBackground(Color.WHITE);

        if (nombre.isEmpty()){
            inputNombre.setBackground(new Color(255,153,153));
        } else {

            TipoAlquilerRepositorio tipoAlquiler = new TipoAlquilerRepositorio();

            TipoAlquiler tipoAlquilerToSave = new TipoAlquiler();
            tipoAlquilerToSave.id_tipo_alquiler = Integer.parseInt(idSelected);
            tipoAlquilerToSave.nombre = nombre;

            try {
                tipoAlquiler.actualizarTipoAlquiler(tipoAlquilerToSave);
                JOptionPane.showMessageDialog(null, "Se actualizo exitosamente");
                mostrarTipoAlquiler();
            } catch (SQLException ex){
                JOptionPane.showMessageDialog(null, "No se pudo actualizar, intentelo de nuevo");
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

