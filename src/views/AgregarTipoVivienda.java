    public void mostrarTipoVivienda(){
            
        try {
            TipoViviendaRepositorio tipoViviendaRepo = new TipoViviendaRepositorio();
            List<TipoVivienda> vivienda = tipoViviendaRepo.List();
            DefaultTableModel DFT = (DefaultTableModel) tblTipoVivienda.getModel();
            DFT.setRowCount(0);
                for(TipoVivienda tipvivi:vivienda){
                    Vector v = new Vector();
                    for (int ii = 1; ii <= vivienda.size(); ii++) {
                        v.add(tipvivi.id_tipo_vivienda);
                        v.add(tipvivi.nombre);                    
                    }
                DFT.addRow(v);
                }
            } catch (Exception e) {
                System.out.println(e);
        }
    }
