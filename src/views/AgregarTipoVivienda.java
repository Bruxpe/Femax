    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        
        ConexionBD con = new ConexionBD();
        String nombre = inputNombre.getText();

        if (nombre.isEmpty()){
            inputNombre.setBackground(new Color(255,153,153));
        } else {
            
            TipoViviendaRepositorio tipoVivienda = new TipoViviendaRepositorio();
            
            TipoVivienda tipoViviendaToSave = new TipoVivienda();
            tipoViviendaToSave.nombre = nombre;
            
            try {
                tipoVivienda.crearTipoVivienda(tipoViviendaToSave);
                JOptionPane.showMessageDialog(null, "Se guardo exitosamente");
                mostrarTipoVivienda();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "No se pudo guardar, intentelo de nuevo");
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblTipoVivienda.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(null, "Debe seleccionar un elemento de la tabla para eliminar");
        }
        else{
            try {
                TipoViviendaRepositorio tipoViviendaRepo = new TipoViviendaRepositorio();
                boolean delete = tipoViviendaRepo.eliminarTipoVivienda(Integer.parseInt(idSelected));
                if(delete){
                    inputNombre.setText("");;
                    idSelected = null;
                    btnUpdate.setEnabled(false);
                    btnSave.setEnabled(true);
                    JOptionPane.showMessageDialog(null, "Se elimino exitosamente");
                    mostrarTipoVivienda();
                }
                else{
                    JOptionPane.showMessageDialog(null, "No se pudo eliminar, intentelo de nuevo");
                }
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "No se pudo eliminar, intentelo de nuevo");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tblTipoViviendaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTipoViviendaMouseClicked
        // TODO add your handling code here:
        int row = tblTipoVivienda.getSelectedRow();
        if(row >= 0){
            btnUpdate.setEnabled(true);
            btnSave.setEnabled(false);
            String id = tblTipoVivienda.getModel().getValueAt(row, 0).toString();
            idSelected = id;
            TipoViviendaRepositorio tipoViviendaRepo = new TipoViviendaRepositorio();
            TipoVivienda v = tipoViviendaRepo.Get(Integer.parseInt(id));            
            inputNombre.setText(v.nombre);
        }
    }//GEN-LAST:event_tblTipoViviendaMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String nombre = inputNombre.getText();
        
        inputNombre.setBackground(Color.WHITE);

        if (nombre.isEmpty()){
            inputNombre.setBackground(new Color(255,153,153));
        } else {
            
            TipoViviendaRepositorio tipoVivienda = new TipoViviendaRepositorio();
            
            TipoVivienda tipoViviendaToSave = new TipoVivienda();
            tipoViviendaToSave.id_tipo_vivienda = Integer.parseInt(idSelected);
            tipoViviendaToSave.nombre = nombre;
            
            try {
                tipoVivienda.actualizarTipoVivienda(tipoViviendaToSave);
                JOptionPane.showMessageDialog(null, "Se guardo exitosamente");
                mostrarTipoVivienda();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "No se pudo guardar, intentelo de nuevo");
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

